// MARK: - Enums
enum WindowSize: String, CaseIterable {
    case small = "small"
    case medium = "medium"
    case large = "large"
    
    var dimensions: (width: CGFloat, height: CGFloat) {
        switch self {
        case .small: return (600, 400)
        case .medium: return (800, 600)
        case .large: return (1200, 800)
        }
    }
}

enum ResponseStyle: String, CaseIterable {
    case concise = "concise"
    case detailed = "detailed"
    case conversational = "conversational"
}

enum ExplanationLevel: String, CaseIterable {
    case beginner = "beginner"
    case intermediate = "intermediate"
    case advanced = "advanced"
}

enum IndexFrequency: String, CaseIterable {
    case realtime = "realtime"
    case fiveMinutes = "fiveMinutes"
    case fifteenMinutes = "fifteenMinutes"
    case manual = "manual"
}

enum AppColorScheme: String, CaseIterable {
    case system = "system"
    case light = "light"
    case dark = "dark"
}

enum AccentColor: String, CaseIterable {
    case blue = "blue"
    case purple = "purple"
    case pink = "pink"
    case red = "red"
    case orange = "orange"
    case yellow = "yellow"
    case green = "green"
    case mint = "mint"
    case teal = "teal"
    case cyan = "cyan"
    
    var color: Color {
        switch self {
        case .blue: return .blue
        case .purple: return .purple
        case .pink: return .pink
        case .red: return .red
        case .orange: return .orange
        case .yellow: return .yellow
        case .green: return .green
        case .mint: return .mint
        case .teal: return .teal
        case .cyan: return .cyan
        }
    }
}

enum FontSize: String, CaseIterable {
    case small = "small"
    case medium = "medium"
    case large = "large"
    
    var scale: CGFloat {
        switch self {
        case .small: return 0.9
        case .medium: return 1.0
        case .large: return 1.1
        }
    }
}

enum CodeFont: String, CaseIterable {
    case sfMono = "sfMono"
    case menlo = "menlo"
    case monaco = "monaco"
    case firaCode = "firaCode"
    
    var fontName: String {
        switch self {
        case .sfMono: return "SF Mono"
        case .menlo: return "Menlo"
        case .monaco: return "Monaco"
        case .firaCode: return "Fira Code"
        }
    }
}
